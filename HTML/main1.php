        document.addEventListener('DOMContentLoaded', function() {
            // All variables and IDs are prefixed with "photo"
            const photoSlider = document.getElementById('photo-slider');
            const photoContainer = document.getElementById('photo-slider-container');
            const photoNav = document.getElementById('photo-slider-nav');

            // Check if the component exists on the page
            if (!photoSlider) return;

            const photoSlides = photoContainer.querySelectorAll('.slide');
            const photoSlideCount = photoSlides.length;
            let currentPhotoSlide = 0;
            let photoAutoScrollInterval;

            // --- 1. Create Navigation Dots ---
            for (let i = 0; i < photoSlideCount; i++) {
                const dot = document.createElement('button');
                // Use black/grey colors for dots
                dot.classList.add('w-3', 'h-3', 'rounded-full', 'transition-all', 'duration-300', 'bg-gray-300');
                dot.setAttribute('aria-label', `Go to photo slide ${i + 1}`);

                dot.addEventListener('click', () => {
                    scrollToPhotoSlide(i);
                });

                photoNav.appendChild(dot);
            }
            const photoDots = photoNav.querySelectorAll('button');

            // --- 2. Update Active Dot ---
            function updateActivePhotoDot(index) {
                photoDots.forEach((dot, i) => {
                    if (i === index) {
                        // Active dot is black
                        dot.classList.remove('bg-gray-300');
                        dot.classList.add('bg-gray-900', 'w-5'); // bg-gray-900 is Tailwind's black
                    } else {
                        // Inactive dot is light grey
                        dot.classList.remove('bg-gray-900', 'w-5');
                        dot.classList.add('bg-gray-300');
                    }
                });
            }

            // --- 3. Scroll to a Specific Slide ---
            function scrollToPhotoSlide(index) {
                const scrollLeft = photoContainer.clientWidth * index;
                photoContainer.scrollTo({
                    left: scrollLeft,
                    behavior: 'smooth'
                });
                currentPhotoSlide = index;
                updateActivePhotoDot(index);
            }

            // --- 4. Automatic Scrolling Logic ---
            function startPhotoAutoScroll() {
                clearInterval(photoAutoScrollInterval);
                photoAutoScrollInterval = setInterval(() => {
                    let nextSlide = (currentPhotoSlide + 1) % photoSlideCount;
                    scrollToPhotoSlide(nextSlide);
                }, 7000); // 7 seconds
            }

            function stopPhotoAutoScroll() {
                clearInterval(photoAutoScrollInterval);
            }

            // --- 5. Update Active Dot on Manual Scroll ---
            let photoScrollTimer;
            photoContainer.addEventListener('scroll', () => {
                clearTimeout(photoScrollTimer);
                photoScrollTimer = setTimeout(() => {
                    const newIndex = Math.round(photoContainer.scrollLeft / photoContainer.clientWidth);
                    if (newIndex !== currentPhotoSlide) {
                        currentPhotoSlide = newIndex;
                        updateActivePhotoDot(currentPhotoSlide);
                    }
                }, 100);
            });

            // --- 6. Pause on Hover/Touch ---
            photoSlider.addEventListener('mouseenter', stopPhotoAutoScroll);
            photoSlider.addEventListenser('mouseleave', startPhotoAutoScroll);
            photoSlider.addEventListener('touchstart', stopPhotoAutoScroll, {
                passive: true
            });
            photoSlider.addEventListener('touchend', startPhotoAutoScroll);

            // --- 7. Initialize ---
            updateActivePhotoDot(0);
            startPhotoAutoScroll();
        });
    </script>
