    <script>
        const canvas = document.getElementById("gradientCanvas");
        const ctx = canvas.getContext("2d");
        canvas.width = window.innerWidth;
        canvas.height = window.innerHeight;

        let colors = ["#ff6b6b", "#ffcc5c", "#88d8b0", "#6b6bff", "#cc5cff"];
        let blobs = Array.from({
            length: 5
        }, () => ({
            x: Math.random() * canvas.width,
            y: Math.random() * canvas.height,
            dx: (Math.random() - 0.5) * 1.5,
            dy: (Math.random() - 0.5) * 1.5
        }));

        function drawBlurryGradient() {
            ctx.clearRect(0, 0, canvas.width, canvas.height);
            let gradientCanvas = document.createElement("canvas");
            let gCtx = gradientCanvas.getContext("2d");
            gradientCanvas.width = canvas.width;
            gradientCanvas.height = canvas.height;

            let gradient = gCtx.createLinearGradient(0, 0, canvas.width, canvas.height);
            colors.forEach((color, index) => {
                gradient.addColorStop(index / (colors.length - 1), color);
            });

            gCtx.fillStyle = gradient;
            gCtx.fillRect(0, 0, canvas.width, canvas.height);

            ctx.globalAlpha = 0.3;
            ctx.filter = "blur(50px)";
            ctx.drawImage(gradientCanvas, 0, 0);
            ctx.filter = "none";
        }

        function animate() {
            drawBlurryGradient();
            blobs.forEach(blob => {
                blob.x += blob.dx;
                blob.y += blob.dy;

                if (blob.x < 0 || blob.x > canvas.width) blob.dx *= -1;
                if (blob.y < 0 || blob.y > canvas.height) blob.dy *= -1;
            });
            requestAnimationFrame(animate);
        }

        animate();
    </script>

